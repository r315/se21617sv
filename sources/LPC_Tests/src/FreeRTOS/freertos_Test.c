/* Kernel includes. */
#include "FreeRTOS.h"
#include "led.h"
#include "task.h"

void vToggleLed(void *arg){

	LED_Init(LED, LED_ON);
	while(1){
		LED_Toggle();
		vTaskDelay(400);
	}

	vTaskDelete(NULL);
}


int FreeRTOS_Test(void){

	xTaskCreate( vToggleLed, "Toggle Led", configMINIMAL_STACK_SIZE, NULL, 0, NULL );

	vTaskStartScheduler();

	while(1){

	}
return 0;
}
